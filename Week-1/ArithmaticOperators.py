# Values
x = 56
y = 4


# Arithmetic Operators
print("Addition:", x + y)        # 56 + 4 = 60
print("Subtraction:", x - y)     # 56 - 4 = 52
print("Multiplication:", x * y)  # 56 * 4 = 224
print("Division:", x / y)        # 56 / 4 = 14.0
print("Floor Division:", x // y) # 56 // 4 = 14
print("Modulus:", x % y)         # 56 % 4 = 0
print("Exponent:", x ** y)       # 56^4


# Assignment Operators 
print("Assignment Operators")
z = x
print("z =", z)
z += y
print("z += y:", z)
z -= y
print("z -= y:", z)
z *= y
print("z *= y:", z)
z /= y
print("z /= y:", z)
z //= y
print("z //= y:", z)
z %= y
print("z %= y:", z)
z **= y
print("z **= y:", z)

# Comparison Operators
print("Comparison Operators:")
print("x == y:", x == y)   # Equal to
print("x != y:", x != y)   # Not equal
print("x > y:", x > y)     # Greater than
print("x < y:", x < y)     # Less than
print("x >= y:", x >= y)   # Greater than or equal
print("x <= y:", x <= y)   # Less than or equal